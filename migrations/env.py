import sys
from os.path import abspath, dirname
import os
from dotenv import load_dotenv
from logging.config import fileConfig
from sqlalchemy import engine_from_config, pool
from alembic import context
from sqlmodel import SQLModel

# Asegurar que el path incluya la app
sys.path.insert(0, abspath(dirname(dirname(__file__))))

# Importar modelos para que Alembic los vea
from app.models.users import User
from app.models.products import Product
from app.models.categories import Category
from app.models.affiliates import AffiliateLink, ClickEvent

load_dotenv()

config = context.config

# --- LÓGICA DE URL PARA RENDER/NEON ---
db_url = os.getenv("DATABASE_URL")
if db_url:
    # Render/Heroku a veces dan postgres://, pero SQLAlchemy pide postgresql://
    if db_url.startswith("postgres://"):
        db_url = db_url.replace("postgres://", "postgresql://", 1)
    
    # Forzar el uso del driver psycopg2 si no está en la URL
    if "postgresql+psycopg2://" not in db_url and "postgresql://" in db_url:
        db_url = db_url.replace("postgresql://", "postgresql+psycopg2://", 1)
        
    config.set_main_option("sqlalchemy.url", db_url)
# ---------------------------------------

if config.config_file_name is not None:
    fileConfig(config.config_file_name)

target_metadata = SQLModel.metadata

def run_migrations_offline() -> None:
    url = config.get_main_option("sqlalchemy.url")
    context.configure(
        url=url,
        target_metadata=target_metadata,
        literal_binds=True,
        dialect_opts={"paramstyle": "named"},
    )
    with context.begin_transaction():
        context.run_migrations()

def run_migrations_online() -> None:
    # Usamos la configuración cargada dinámicamente
    connectable = engine_from_config(
        config.get_section(config.config_ini_section, {}),
        prefix="sqlalchemy.",
        poolclass=pool.NullPool,
    )

    with connectable.connect() as connection:
        context.configure(
            connection=connection, target_metadata=target_metadata
        )
        with context.begin_transaction():
            context.run_migrations()

if context.is_offline_mode():
    run_migrations_offline()
else:
    run_migrations_online()

